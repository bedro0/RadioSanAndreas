    <h1>About the project:</h1>
    <h2>Prologue</h2>
    <p>
        In Grand Theft Auto: San Andreas, a popular videogame by Rockstar Games released in the year 2004, there are a total of 11 radio stations. You can hear them everytime you get into a vehicle in game.    
    </p>

    <p>
        The stations are separated by genre and feature various hosts. Rockstar did a very good job of realism when it comes to radio stations in this game. They even invited celebrities to voice certain radio personalities, like Tommy "The Nightmare" Smith on K-DST voiced by Axl Rose. This level of attention-to-detail makes the game incredibly immersive and transforms its world into a living thing. So much so that some fans actually decided that they wanted to take this music to go, to listen to it in real life, on a commute, or on a walk. This project was born of a similar idea and aims to recreate the music playback in the same way it's featured in the game itself.
    </p>
